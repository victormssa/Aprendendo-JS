/* Operadores de comparação
> Maior 
>= Maior ou igual
< Menor
<= Menor ou igual
== Igualdade | != (= Atribuição)
=== Igualdade estrita (Valor e tipo)
!= Diferente (Valor)
!== Diferente estrita (Valor e tipo)
*/

console.log(10 > 5);
console.log(10 >= 5);
console.log(10 < 5);
console.log(10 <= 5);
console.log(10 == 5);
console.log(10 === 5);
console.log(10 != 5);
console.log(10 !== 5);

//Type correction
console.log('10' == 10);
//False pelo tipo deles não serem equivalentes
console.log('10' === 10);